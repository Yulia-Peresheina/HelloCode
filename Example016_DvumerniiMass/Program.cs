void FillImg(int row, int col)
{
    if (pic[row, col] == 0)
    {    pic[row, col] = 1;
        FillImg(row - 1, col);
        FillImg(row, col - 1);
        FillImg(row + 1, col);
        FillImg(row, col + 1);
    }
}

