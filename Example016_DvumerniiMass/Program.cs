void PrintImg(int[,] matr)
{
    for (int i = 0; i < matr.GetLength(0); i++)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            if (matr[i, j] == 0)
                Console.Write(" ");
            else
                Console.Write("+");
        }
        Console.WriteLine();
    }

}
